void main(List<String> arguments) {
  // variable

  // String
  String nameUser = "Diego";
  // Integer
  int x = 41;
  //  Dynamic
  dynamic firstName = "Tim";

  // Const, final
  const String fullName = "Diego Auza";
  final String nickName =
      "J"; // if the data is going to change we use final on a run

  // declaring and not using

  var myName;
  print(myName);
  print(firstName);
  print(x);
  print(nameUser);

  // Data type
  // Numbers, Strings , Booleans, Lists, Map, Dynamic
  // Strings
  String firstNameData = "Diego";
  print("String $firstNameData");
  // Integers
  int myNumber = 35;
  print("My number is: $myNumber");
  // Double
  double otherNumberDouble = 19.95;
  print(otherNumberDouble);
  // Booleans
  bool myBool = false;
  print("This is a boolean: $myBool");
  //Dynamic
  dynamic fullNameDynamic = "Jhon Elder";
  print(fullNameDynamic);
}
